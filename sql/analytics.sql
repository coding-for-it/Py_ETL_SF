USE DATABASE ETL_DB;

USE SCHEMA SALES_SCHEMA;


CREATE OR REPLACE STAGE SALES_STAGE;

LIST @SALES_STAGE;

CREATE OR REPLACE TABLE SALES_RAW
(

ORDER_ID INT,

CUSTOMER_NAME STRING,

PRODUCT STRING,

QUANTITY INT,

PRICE FLOAT,

ORDER_DATE DATE

);

COPY INTO SALES_RAW

FROM @SALES_STAGE/sales.csv

FILE_FORMAT =

(
TYPE='CSV',

SKIP_HEADER=1

);

SELECT *

FROM SALES_RAW;

CREATE OR REPLACE TABLE SALES_FINAL
(

ORDER_ID INT,

CUSTOMER_NAME STRING,

PRODUCT STRING,

QUANTITY INT,

PRICE FLOAT,

ORDER_DATE DATE,

TOTAL_SALES FLOAT

);

CREATE OR REPLACE PROCEDURE LOAD_SALES()

RETURNS STRING

LANGUAGE SQL

AS

$$

BEGIN


INSERT INTO SALES_FINAL


SELECT

ORDER_ID,

CUSTOMER_NAME,

PRODUCT,

QUANTITY,

PRICE,

ORDER_DATE,

QUANTITY * PRICE AS TOTAL_SALES


FROM SALES_RAW;



RETURN 'DATA LOADED SUCCESSFULLY';


END;


$$;

CALL LOAD_SALES();

SELECT *

FROM SALES_FINAL;


-- Procedures

-- Revenue
SELECT

SUM(TOTAL_SALES)

FROM SALES_FINAL;

-- Product Ranking
SELECT

PRODUCT,

SUM(TOTAL_SALES)

FROM SALES_FINAL

GROUP BY PRODUCT

ORDER BY 2 DESC;