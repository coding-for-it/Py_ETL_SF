USE DATABASE ETL_DB;

USE SCHEMA SALES_SCHEMA;



-- Check data

SELECT *

FROM SALES;



-- Total Revenue

SELECT

SUM(TOTAL_SALES) AS TOTAL_REVENUE

FROM SALES;



-- Product wise sales


SELECT

PRODUCT,

SUM(TOTAL_SALES) AS SALES

FROM SALES

GROUP BY PRODUCT

ORDER BY SALES DESC;



-- Highest selling product


SELECT

PRODUCT,

SUM(QUANTITY) AS TOTAL_QTY

FROM SALES

GROUP BY PRODUCT

ORDER BY TOTAL_QTY DESC;



-- Monthly sales


SELECT

MONTH(ORDER_DATE) AS MONTH,

SUM(TOTAL_SALES)

FROM SALES

GROUP BY MONTH;